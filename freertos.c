/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN StartDefaultTask */
  MAX7219_Init();
  uint8_t level = 0;
  /* Infinite loop */
  for(;;)
  {
    if(HAL_I2C_IsDeviceReady(&hi2c1, BH1750_ADDR, 3, 100) == HAL_OK) //BH1750이 준비되어있는지 확인
      {
          lux = BH1750_ReadLight(&hi2c1); // 조도센서 읽기
    }

    if(lux < 5)
    {
      level = 1;
    }
    else if(lux < 10)
    {
      level = 2;
    }
    else if(lux < 50)
    {
      level = 3;
    }
    else if(lux > 900)
    {
      level = 8;
    }
    else
    {
      level = lux / 100 + 3;
    }

    MAX7219_DisplayLevel(level);
    osDelay(1000);

    if(level <= 2)
    {
      MAX7219_DisplayMoon();
      printf("조도 센서 측정 결과: %d, level:%d, 어두움\r\n", lux, level);
    }
    else
    {
      MAX7219_DisplaySun();
      printf("조도 센서 측정 결과: %d, level:%d, 밝음\r\n", lux, level);
    }



    osDelay(1000);

  }
  /* USER CODE END StartDefaultTask */
}

